 INICIALIZACAO PROC
 ; PUSHALL
  ; IMPRIME MENSAGEM DE BEM-VINDO
  MOV AH, 09H
  MOV DX, OFFSET MSG_BEMVINDO
  INT 21H
  PULA_LINHA
  PULA_LINHA
ENTRADA_OPCAO:
  ; IMPRIME MENSAGEM DE MODO DE JOGO
  MOV DX, OFFSET MSG1
  INT 21H
  MOV AH, 01H
  INT 21H
  AND AL, 0FH
  CMP AL, 0
  JZ VOLTAR
  CMP AL, 1
  JE VOLTAR
  MOV AH, 09H
  MOV DX, OFFSET MSG2
  INT 21H
  JMP ENTRADA_OPCAO
VOLTAR:
      ; POPALL
  RET 
 INICIALIZACAO ENDP
VER_VIT PROC
 ;Verificação horizontal
  XOR BX,BX
  MOV CL,3
DENOVO1:
  XOR DX,DX
  XOR SI,SI
  MOV CH,3
AGAIN1:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JNE CONT1
  INC DH
CONT1:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,6Fh
  JNE SAI1
  INC DL
SAI1:
  INC SI
  DEC CH
  JNZ AGAIN1
  CMP DH,3
  JE CORRECTION1
  CMP DL,3
  JE CORRECTION2
  ADD BX,3
  DEC CL
  JNZ DENOVO1
  ;Verificação vertical
  XOR SI,SI
  MOV CL,3
DENOVO2:
  XOR DX,DX
  XOR BX,BX
  MOV CH,3
AGAIN2:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JNE CONT2
  INC DH
CONT2:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,6Fh
  JNE SAI12
  INC DL
SAI12:
  ADD BX,3
  DEC CH
  JNZ AGAIN2
  CMP DH,3
CORRECTION1:
  JE VIT_JOG
  CMP DL,3
CORRECTION2:
  JE VIT_JOG2_COMP
  INC SI
  DEC CL
  JNZ DENOVO2
  XOR BX,BX
  XOR SI,SI
  XOR DX,DX
  MOV CX,3
AGAIN3:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JNE CONT3
  INC DH
CONT3:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,6Fh
  JNE SAI3
  INC DL
SAI3:
  ADD BX,3
  INC SI
  LOOP AGAIN3
  CMP DH,3
  JE VIT_JOG
  CMP DL,3
  JE VIT_JOG2_COMP
  XOR DX,DX
  XOR BX,BX
  MOV SI,2
  MOV CX,3
AGAIN4:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JNE CONT4
  INC DH
CONT4:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JNE SAI4
  INC DL
SAI4:
  ADD BX,3
  DEC SI
  LOOP AGAIN4
  CMP DH,3
  JE VIT_JOG
  CMP DL,3
  JE VIT_JOG2_COMP
  JMP RETORNA
VIT_JOG:
  CLS
  MOV AX,1
  JMP FINAL_VIT
VIT_JOG2_COMP:
  CLS
  MOV AX,2
  JMP FINAL_VIT
RETORNA:
  MOV AX,0
FINAL_VIT:
  RET
 VER_VIT ENDP
 RESET_MATRIZ PROC
  PUSH BX
  PUSH SI
  XOR CX,CX
  XOR BX,BX
  MOV DL,31h
  MOV CH,3
RESET_MAT2:
  XOR SI,SI
  MOV CL,3
RESET_MAT1:
  MOV MATRIZ[BX][SI],DL
  INC DL
  INC SI
  DEC CL
  JNZ RESET_MAT1
  ADD BX,3
  DEC CH
  JNZ RESET_MAT2
  POP SI
  POP BX
  RET
 RESET_MATRIZ ENDP
 BOT_JOG PROC
  XOR AH,AH
  XOR BX,BX
  MOV CH,3
NOVAMENTE2_BOT1:
  XOR DX,DX
  XOR SI,SI
  MOV CL,3
NOVAMENTE_BOT1:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JNE CONT_BOT1
  INC DX
CONT_BOT1:
  INC SI
  DEC CL
  JNZ NOVAMENTE_BOT1
  CMP DX,2
  JE VER_INSERI_BOT_LINHA
CONT_VER1:
  ADD BX,3
  DEC CH
  JNZ NOVAMENTE2_BOT1
  XOR SI,SI
  MOV CH,3
NOVAMENTE2_BOT2:
  XOR DX,DX
  XOR BX,BX
  MOV CL,3
NOVAMENTE_BOT2:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JNE CONT_BOT2
  INC DX
CONT_BOT2:
  ADD BX,3
  DEC CL
  JNZ NOVAMENTE_BOT2
  CMP DX,2
  JE VER_INSERI_BOT_COLUNA
CONT_VER2:
  INC SI
  DEC CH
  JNZ NOVAMENTE2_BOT2
  XOR SI,SI
  XOR DX,DX
  XOR BX,BX
  MOV CX,3
NOVAMENTE_BOT3:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JNE CONT_BOT3
  INC DX
CONT_BOT3:
  ADD BX,3
  INC SI
  DEC CX
  JNZ NOVAMENTE_BOT3
  CMP DX,2
  JE VER_INSERI_BOT_DIAG1
CONT_VER3:
  MOV SI,2
  XOR DX,DX
  XOR BX,BX
  MOV CX,3
NOVAMENTE_BOT4:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JNE CONT_BOT4
  INC DX
CONT_BOT4:
  ADD BX,3
  DEC SI
  DEC CX
  JNZ NOVAMENTE_BOT4
  CMP DX,2
  JE VER_INSERI_BOT_DIAG2
  JMP FIM_BOT
VER_INSERI_BOT_LINHA:
  XOR SI,SI
  MOV DX,3
VER_ELEM1:
  MOV AL,MATRIZ[BX][SI]
  CMP AL,78h
  JE CONT_BOT1_2
  MOV AL,MATRIZ[BX][SI]
  CMP AL,6Fh
  JE CONT_VER1
  MOV AH,1
  MOV MATRIZ[BX][SI],6Fh
  JMP FIM_BOT
CONT_BOT1_2:
