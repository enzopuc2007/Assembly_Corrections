 DETERM PROC
  CMP DX,3
  JBE CASE1
  CMP DX,6
  JBE CASE2
  CMP DX,9
  JBE CASE3
CASE1:
  DEC DX
  MOV SI,DX
  MOV BX,0
CASE2:
  DEC DX
  MOV SI,DX
  MOV BX,3
CASE3:
  DEC DX
  MOV SI,DX
  MOV BX,6
  RET
 DETERM ENDP
