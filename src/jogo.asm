 JOGO_MULTIPLAYER PROC
  MOV CX,9
JOGO1:
  PUSH CX
  CLS
  TEXTO_INICIO
  PULA_LINHA
  PULA_LINHA
  ORDE_1
  PULA_LINHA
  PULA_LINHA
  CALL IMPRIME_MATRIZ
  PULA_LINHA
  CALL CAPTA_VERIFICA
  PULA_LINHA
  CMP AX,1
  JE SAI_DEFINITIVO
  CMP AX,2
  JE SAI_DEFINITIVO
  POP CX
  LOOP JOGO1
  CALL IMPRIME_MATRIZ
  RET
SAI_DEFINITIVO:
  POP CX
  CALL IMPRIME_MATRIZ
  RET
 JOGO_MULTIPLAYER ENDP
 JOGO_ROBO PROC
  MOV CX,9
JOGO2:
  PUSH CX
  CLS
  TEXTO_INICIO
  PULA_LINHA
  PULA_LINHA
  ORDE_2
  ; ORDE_1
  PULA_LINHA
  PULA_LINHA
  CALL IMPRIME_MATRIZ
  PULA_LINHA
  TEST CX,1
  JNZ VEZ_JOGADOR
  CALL CENTRAL_BOT
  JMP CONT_JOGO_2
VEZ_JOGADOR:
  CALL CAPTA_VERIFICA
  PULA_LINHA
  CMP AX,1
CORRECTION3:
  JE SAI_DEFINITIVO
  CMP AX,2
  JE CORRECTION3
CONT_JOGO_2:
  POP CX
  LOOP JOGO2
  CALL IMPRIME_MATRIZ
  RET
 JOGO_ROBO ENDP

 CENTRAL_BOT PROC
  CALL BOT_JOG
  CALL VER_VIT
  POS_JOGADOR
  RET
 CENTRAL_BOT ENDP
 CAPTA_VERIFICA PROC
VOLTA:
  MOV AH,9
  LEA DX,MSG_CAPTA_JOG
  INT 21h
  MOV AH,1
  INT 21h
  XOR BX,BX
  XOR SI,SI
  AND AL,0Fh
  TEST AL,15
  JZ ERRO
  CMP AL,3
  JBE ROT1
  CMP AL,6
  JBE ROT2
  CMP AL,9
  JBE ROT3
ERRO:
  TENTE_NOVAMENTE
  JMP VOLTA
ROT1:
  MOV BL,AL
  DEC BL
  MOV SI,BX
  MOV BL,0
  JMP TEST1
ROT2:
  MOV BL,AL
  SUB BL,4
  MOV SI,BX
  MOV BL,3
  JMP TEST1
ROT3:
  MOV BL,AL
  SUB BL,7
  MOV SI,BX
  MOV BL,6
TEST1:
  CALL VERIFICA_MATRIZ
  TEST AX,1
  JZ CONT_MAT
  TENTE_NOVAMENTE
  JMP VOLTA
CONT_MAT:
  CALL INSERE_MATRIZ
  CALL VER_VIT
  CMP AX,0
  JE FINALIZA
  CMP AX,1
  JE VIT_1
  CMP AX,2
  JE VIT_2
VIT_1:
  CLS
  MSG_VIT_1
  JMP FINALIZA
VIT_2:
  CLS
  MSG_VIT_2
FINALIZA:
  RET
 CAPTA_VERIFICA ENDP
 INSERE_MATRIZ PROC
  TEST CX,1
  JNZ JOG1
  MOV MATRIZ[BX][SI],6Fh
  JMP FINAL
JOG1:
  MOV MATRIZ[BX][SI],78h
FINAL: 
  RET
 INSERE_MATRIZ ENDP
 VERIFICA_MATRIZ PROC
  XOR AX,AX
  CMP MATRIZ[BX][SI],78h
  JZ SAI8
  CMP MATRIZ[BX][SI],6Fh
  JZ SAI8
  CMP MATRIZ[BX][SI],31h
  JAE CONTINUA
  CMP MATRIZ[BX][SI],39h
  JBE CONTINUA
SAI8:
  MOV AX,1
CONTINUA:
  RET
 VERIFICA_MATRIZ ENDP
